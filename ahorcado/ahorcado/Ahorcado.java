    private void detenerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detenerActionPerformed
        inicio = java.applet.Applet.newAudioClip(getClass().getResource("/ahorcado/tonos/2.mp3"));
        inicio.play();
        if (play == 1) {
            btnPlay.setText("Iniciar Musica");
            btnPlay.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/play1.png")));
            controlM.setBackground(Color.green);
            controlM.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/play.png")));
            game.inicio.stop();
            play = 0;
        } else {
            btnPlay.setText("Iniciar Musica");
            btnPlay.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/play1.png")));
            controlM.setBackground(Color.green);
            controlM.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/play.png")));
        }
        bloque();
        cant.setText("");
        Palabra_oculta.setText("????????????");
        Error_Icon.setIcon(new ImageIcon(getClass().getResource("/ahorcado/ahorcado/vidas/v8.png")));
        nuevo.setEnabled(true);
        detener.setEnabled(false);
        JOptionPane.showMessageDialog(null, "        Â¡ Juego Detenido !\nPuedes Empezar un Nuevo Juego", "Detener", JOptionPane.ERROR_MESSAGE);
        inicio = java.applet.Applet.newAudioClip(getClass().getResource("/ahorcado/tonos/2.mp3"));
        inicio.play();
    }//GEN-LAST:event_detenerActionPerformed

