    private void close() {
        if (JOptionPane.showConfirmDialog(rootPane, "Â¿Salir de la aplicaciÃ³n?",
                "Ahorcado", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE) == JOptionPane.YES_OPTION) {
            audio1 = java.applet.Applet.newAudioClip(getClass().getResource("/ahorcado/tonos/2.mp3"));
            audio1.play();
            System.exit(0);
        } else {
            audio1 = java.applet.Applet.newAudioClip(getClass().getResource("/ahorcado/tonos/2.mp3"));
            audio1.play();
            setDefaultCloseOperation(0);
        }
    }

