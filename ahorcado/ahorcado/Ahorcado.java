    private void controlMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_controlMActionPerformed
        inicio = java.applet.Applet.newAudioClip(getClass().getResource("/ahorcado/tonos/2.mp3"));
        inicio.play();
        if (play == 1) {
            btnPlay.setText("Iniciar Musica");
            btnPlay.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/play1.png")));
            controlM.setBackground(Color.green);
            controlM.setToolTipText("Iniciar");
            controlM.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/play.png")));
            game.inicio.stop();
            play = 0;
        } else {
            btnPlay.setText("Detener Musica");
            btnPlay.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/stop1.png")));
            controlM.setBackground(Color.red);
            controlM.setToolTipText("Detener");
            controlM.setIcon(new ImageIcon(getClass().getResource("/ahorcado/mas/stop.png")));
            game.inicio.play();
            play = 1;
        }
    }//GEN-LAST:event_controlMActionPerformed

