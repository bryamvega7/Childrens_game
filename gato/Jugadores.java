    void pedirNombre() {

        if (j1.getText().equals("") || j2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "¡Ingresar ambos\n    jugadores!", "TRES EN RAYA", 0,
                    new javax.swing.ImageIcon(getClass().getResource("/gato/aj.png")));
            inicio = java.applet.Applet.newAudioClip(getClass().getResource("/gato/2.mp3"));
            inicio.play();
        } else {
            if (j1.getText().equalsIgnoreCase(j2.getText())) {
                JOptionPane.showMessageDialog(null, "¡Asignar nombres diferentes\n  para los jugadores!", "TRES EN RAYA", 0,
                        new javax.swing.ImageIcon(getClass().getResource("/gato/aj.png")));
                inicio = java.applet.Applet.newAudioClip(getClass().getResource("/gato/2.mp3"));
                inicio.play();
                j1.setText("");
                j2.setText("");
            } else {
                Tablero t = new Tablero();
                dispose();
                t.tit.setText("Tú turno " + j1.getText());
                t.tit.setForeground(Color.yellow);
                t.jug1.setText(j1.getText());
                t.jug2.setText(j2.getText());               
                t.setVisible(true);
                JOptionPane.showMessageDialog(null, "¡ Que gane el mejor !","TRES EN RAYA",JOptionPane.INFORMATION_MESSAGE);
                
            }
        }

    }

