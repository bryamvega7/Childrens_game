    public class progreso implements ActionListener {

        public void actionPerformed(ActionEvent evt) {
            int n = jProgressBar1.getValue();
            if (n < 100) {
                n++;
                jProgressBar1.setValue(n);
                carga.setText(n + "%");
            } else {
                timer.stop();
                acierto = java.applet.Applet.newAudioClip(getClass().getResource("/ahorcado/tonos/acierto.wav"));
                acierto.play();
                JOptionPane.showMessageDialog(null, "ยก Progreso Finalizado !", "Mensaje", 0,
                        new javax.swing.ImageIcon(getClass().getResource("/principal/azulC.png")));
                audio1 = java.applet.Applet.newAudioClip(getClass().getResource("/principal/2.mp3"));
                audio1.play();
                dispose();
                audio.stop();
                Ventana v = new Ventana();
                v.setVisible(rootPaneCheckingEnabled);
            }
        }
    }

