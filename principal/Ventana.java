    public void llenarMenu() {
        java.util.List<Avatar> avatars = new ArrayList<Avatar>();
        avatars.add(new Avatar("Rompecabezas", loadImage("/principal/imagenes/rom.png")));
        avatars.add(new Avatar("Pares", loadImage("/principal/imagenes/par.png")));
        avatars.add(new Avatar("Tres en Raya", loadImage("/principal/imagenes/gat.png")));
        avatars.add(new Avatar("Ahorcado", loadImage("/principal/imagenes/ahor.png")));
        avatars.add(new Avatar("Matemáticas", loadImage("/principal/imagenes/mat.png")));
        avatars.add(new Avatar("Alfabeto", loadImage("/principal/imagenes/alf.png")));
        avatars.add(new Avatar("Salir", loadImage("/principal/imagenes/salir.png")));
        av.setAvatars(avatars);
    }

    public static Image loadImage(String fileName) {
        try {
            return ImageIO.read(Ventana.class.getResource(fileName));
        } catch (Exception e) {
            return null;
        }
    }

    public void llamarMenu() {
        if (av.getSelectedtitulo().equals("Rompecabezas")) {
            this.dispose();
            new rompecabezas.rompecabezas.InicioR().setVisible(true);
        }
        if (av.getSelectedtitulo().equals("Pares")) {
            this.dispose();
            new pares.pares.InicioP().setVisible(true);
        }
        if (av.getSelectedtitulo().equals("Tres en Raya")) {
            this.dispose();
            new gato.Jugadores().setVisible(true);
        }
        if (av.getSelectedtitulo().equals("Ahorcado")) {
            this.dispose();
            new ahorcado.ahorcado.Ahorcado().setVisible(true);
        }
        if (av.getSelectedtitulo().equals("Matemáticas")) {
            this.dispose();
            new matematicas.inicioMat().setVisible(true);
        }
        if (av.getSelectedtitulo().equals("Alfabeto")) {
            this.dispose();
            new alfabeto.Alfabeto().setVisible(true);
        }
        if (av.getSelectedtitulo().equals("Salir")) {
            close();
        }

    }

